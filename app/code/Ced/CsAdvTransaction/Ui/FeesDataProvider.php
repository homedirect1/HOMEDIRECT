namespace Ced\CsAdvTransaction\Ui;

use Ced\CsAdvTransaction\Model\ResourceModel\Fee\CollectionFactory;

/**
 * Class FeesDataProvider
 * @package Ced\CsAdvTransaction\Ui
 */
class FeesDataProvider extends \Magento\Ui\DataProvider\AbstractDataProvider
{
    /**
     * Tickets collection
     *
     * @var \Magento\Catalog\Model\ResourceModel\Product\Collection
     */
    protected $collection;

    /**
     * FeesDataProvider constructor.
     * @param string $name
     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param CollectionFactory $collectionFactory
     * @param array $addFieldStrategies
     * @param array $addFilterStrategies
     * @param array $meta
     * @param array $data
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        CollectionFactory $collectionFactory,
        array $addFieldStrategies = [],
        array $addFilterStrategies = [],
        array $meta = [],
        array $data = []
    )
    {
        parent::__construct($name, $primaryFieldName, $requestFieldName, $meta, $data);
        $this->collection = $collectionFactory->create();
    }

    /**
     * Get data
     *
     * @return array
     */
    public function getData()
    {
        $collection = $this->collection->getData();
        return [
            'totalRecords' => $this->collection->getSize(),
            'items' => array_values($collection),
        ];
    }

}
